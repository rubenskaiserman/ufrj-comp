#include <stdio.h>
int main() {
    int inteiro = 10;
    float flutuante = 2.5;
    double dupla_precisao = 5.78934;
    char caractere = 'A';
    printf("Valor inteiro: %d\n", inteiro);
    printf("Valor flutuante: %.2f\n", flutuante);
    printf("Valor de dupla precisao: %lf\n", dupla_precisao);
    printf("Valor do caractere: %c\n", caractere);
    return 0;
}